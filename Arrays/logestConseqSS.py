class Solution:
    
    # arr[] : the input array
    # N : size of the array arr[]
    
    #Function to return length of longest subsequence of consecutive integers.
    def findLongestConseqSubseq(self,arr, N):
        #code here
        d={}
        # prev={}
        res=0
        for ele in arr:
            d[ele]=1
        for key in d.keys():
            i=1
            temp=1
            if key-1 not in d:
                while key+i in d:
                    temp+=1
                    i+=1
                res=max(res,temp)
        return res
                

